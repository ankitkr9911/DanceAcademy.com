// Import Mongoose
const mongoose = require('mongoose');

// Define a schema
const contactSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    age: {
        type: Number,
        required: true
    },
    gender: {
        type: String,
        required: true
    },
    phoneNo: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    address: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    }
});

// Create a model
const Contact = mongoose.model('Contact', contactSchema);

// Export the model
module.exports = Contact;
